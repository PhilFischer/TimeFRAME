test_that("GP model works", {
  d <- data.frame(X = c(5,0), Y = c(0, 5), DX = c(1,1), DY = c(1,1), row.names = c("A", "B"))
  m <- frame_model(d)
  t <- seq(0, 1, length.out = 3)
  x <- data.frame(x = c(0.9, 1, 0.8), y = c(0.1, 0.3, 0.1))
  sd <- x + 1
  testthat::expect_error(fit <- fit_gp(m, x, t, eta = 0))
  testthat::expect_no_error(fit <- fit_gp(m, x, t, eta = 1))
  testthat::expect_vector(coef(fit, point.est = "mean"))
  testthat::expect_vector(coef(fit, point.est = "median"))
  testthat::expect_vector(as.data.frame(fit))
  testthat::expect_no_error(fit <- fit_gp(m, x, t, sd = sd))
  testthat::expect_vector(coef(fit, point.est = "mean"))
  testthat::expect_vector(coef(fit, point.est = "median"))
  testthat::expect_vector(as.data.frame(fit))
  testthat::expect_no_error(fit <- fit_gp(m, x, t, sd = sd, eta = 1))
  testthat::expect_vector(coef(fit, point.est = "mean"))
  testthat::expect_vector(coef(fit, point.est = "median"))
  testthat::expect_vector(as.data.frame(fit))
  testthat::expect_no_error(fit <- fit_gp(m, x, t, eta = c(1,1)))
  testthat::expect_vector(coef(fit, point.est = "mean"))
  testthat::expect_vector(coef(fit, point.est = "median"))
  testthat::expect_vector(as.data.frame(fit))
  testthat::expect_no_error(fit <- fit_gp(m, x, t, sd = sd, eta = c(1,1)))
  testthat::expect_vector(coef(fit, point.est = "mean"))
  testthat::expect_vector(coef(fit, point.est = "median"))
  testthat::expect_vector(as.data.frame(fit))
})

